export const resolveColor = (color: string) => {
  const map: Record<string, string> = {
    info: 'blue',
    success: 'emerald',
    warning: 'amber',
    gray: 'slate',
  }
  const safeName = map[color] || color
  return `var(--color-${safeName}-500)`
}

